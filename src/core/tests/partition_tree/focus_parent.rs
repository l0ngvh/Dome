#[test]
fn focus_parent_noop() {
    let mut hub = setup();
    let before = snapshot(&hub);
    hub.focus_parent();
    assert_eq!(before, snapshot(&hub));

    let mut hub = setup();
    hub.insert_float(
        hub.current_workspace(),
        Dimension::new(
            Length::new(10.0),
            Length::new(5.0),
            Length::new(30.0),
            Length::new(20.0),
        ),
        titled("w7"),
    );
    let before = snapshot(&hub);
    hub.focus_parent();
    assert_eq!(before, snapshot(&hub));
}
